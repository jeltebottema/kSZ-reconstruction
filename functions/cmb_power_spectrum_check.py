    ax.set_title('Small Box: Misses Acoustic Peaks', fontweight='bold', fontsize=12)
    ax.legend(fontsize=10, loc='upper right')
    ax.grid(True, alpha=0.3)
    ax.set_xlim([0, 3000])
    ax.set_ylim([0, 7000])
    
    # Mark acoustic peaks
    for peak in [220, 540, 810]:
        ax.axvline(x=peak, color='gray', linestyle=':', alpha=0.5)
    
    # Panel (1,1): Power spectrum - large box (shows BAOs!)
    ax = axes[1, 1]
    # Theory
    ax.plot(ells[mask_theory], Dl_lensed[mask_theory], 'b-', linewidth=2, 
            label='Theory (CAMB)', alpha=0.8)
    # Measured from large box
    mask_large = (ell_meas_large >= 10) & (ell_meas_large <= 3000)
    ax.plot(ell_meas_large[mask_large], Dl_meas_large[mask_large], 'go', markersize=4, 
            alpha=0.6, label=f'Large box (ℓ_min≈{ell_min_large:.0f})')
    
    # Mark ℓ_min for large box
    ax.axvline(x=ell_min_large, color='green', linestyle='--', linewidth=2, alpha=0.7,
               label=f'ℓ_min (large box)')
    
    ax.set_xlabel('Multipole ℓ', fontsize=14)
    ax.set_ylabel('$D_\\ell$ [µK²]', fontsize=14)
    ax.set_title('Large Box: Recovers Acoustic Peaks!', fontweight='bold', fontsize=12)
    ax.legend(fontsize=10, loc='upper right')
    ax.grid(True, alpha=0.3)
    ax.set_xlim([0, 3000])
    ax.set_ylim([0, 7000])
    
    # Mark acoustic peaks
    for i, peak in enumerate([220, 540, 810]):
        ax.axvline(x=peak, color='purple', linestyle=':', alpha=0.7)
        ax.text(peak+20, 6000-i*800, f'ℓ≈{peak}', fontsize=10, color='purple')
    
