    plt.close()
    print(f"  Saved: {outfile}")
    
    # =========================================================================
    # 5. Print summary
    # =========================================================================
    print("\n" + "="*80)
    print("SUMMARY")
    print("="*80)
    
    # Find acoustic peaks
    print("\nAcoustic Peak Positions (from CAMB):")
    for i, peak_ell in enumerate([220, 540, 810, 1120, 1450]):
        if peak_ell < len(Dl_lensed):
            print(f"  Peak {i+1}: ℓ ≈ {peak_ell}, D_ℓ ≈ {Dl_lensed[peak_ell]:.1f} µK²")
    
    print("\n" + "-"*60)
    print("BAO RESOLUTION ANALYSIS:")
    print("-"*60)
    print(f"\n  Your kSZ simulation box:")
    print(f"    Physical size: 500 Mpc/h = {box_Mpc_small:.1f} Mpc at z = {z_mean}")
    print(f"    Comoving distance: χ(z={z_mean}) = {chi_Mpc:.0f} Mpc")
    print(f"    Angular size: θ = L/χ = {np.degrees(theta_x_small):.2f}° = {np.degrees(theta_x_small)*60:.0f} arcmin")
    print(f"    Minimum ℓ: ℓ_min ≈ 2π/θ = {ell_min_small:.0f}")
    
