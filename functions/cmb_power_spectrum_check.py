def get_cmb_power_spectra(lmax=5000):
    """
    Get CMB power spectra from CAMB.
    
    Returns D_ℓ = ℓ(ℓ+1)C_ℓ/(2π) in µK² for both lensed and unlensed.
    """
    print("Computing CMB power spectra with CAMB...")
    
    # Set up CAMB parameters
    pars = camb.set_params(
        H0=H0_KM_S_MPC,
        ombh2=OMEGA_B0 * (H0_KM_S_MPC/100)**2,
        omch2=(OMEGA_M0 - OMEGA_B0) * (H0_KM_S_MPC/100)**2,
        tau=0.054,
        As=2.1e-9,
        ns=0.965,
        lmax=lmax,
        lens_potential_accuracy=1
    )
    
    results = camb.get_results(pars)
    powers = results.get_cmb_power_spectra(pars, CMB_unit='muK')
    
    # Extract spectra (CAMB returns D_ℓ directly)
    ells = np.arange(powers['total'].shape[0])
    
    # TT is first column (index 0)
    Dl_lensed = powers['total'][:, 0]
    Dl_unlensed = powers['unlensed_scalar'][:, 0]
    
    # Also get EE, TE for completeness
    Dl_EE_lensed = powers['total'][:, 1]
    Dl_TE_lensed = powers['total'][:, 3]
    
    print(f"  Computed spectra up to ℓ = {lmax}")
    print(f"  Peak D_ℓ(TT) ≈ {Dl_lensed.max():.1f} µK² at ℓ ≈ {ells[np.argmax(Dl_lensed)]}")
    
    return {
        'ells': ells,
        'Dl_TT_lensed': Dl_lensed,
        'Dl_TT_unlensed': Dl_unlensed,
        'Dl_EE_lensed': Dl_EE_lensed,
        'Dl_TE_lensed': Dl_TE_lensed,
    }


def compute_comoving_distance(z):
    """Compute comoving distance to redshift z in Mpc."""
    c_km_s = 299792.458
    
    def E(zp):
        return np.sqrt(OMEGA_M0 * (1 + zp)**3 + OMEGA_L0)
    
    chi_integral, _ = quad(lambda zp: 1.0 / E(zp), 0, z)
    chi_Mpc = (c_km_s / H0_KM_S_MPC) * chi_integral
    
    return chi_Mpc


def generate_flat_sky_cmb(nx, ny, Lx_Mpc, Ly_Mpc, z_mean, lensed=True, seed=42):
    """
    Generate a flat-sky CMB temperature anisotropy map.
    
    Parameters:
    -----------
    nx, ny : int
        Number of pixels in x and y directions
    Lx_Mpc, Ly_Mpc : float
        Physical box size in Mpc (not Mpc/h)
    z_mean : float
        Mean redshift (for angular diameter distance)
    lensed : bool
        Use lensed or unlensed C_ℓ
    seed : int
        Random seed
        
    Returns:
    --------
    cmb_map : ndarray (ny, nx)
        CMB temperature map in µK
    """
    np.random.seed(seed)
    
    # Comoving distance
    chi_Mpc = compute_comoving_distance(z_mean)
    
    # Angular size of box in radians
    theta_x = Lx_Mpc / chi_Mpc
    theta_y = Ly_Mpc / chi_Mpc
    
    # Pixel size in radians
    dx_rad = theta_x / nx
    dy_rad = theta_y / ny
    
    # Get C_ℓ from CAMB
    ell_max = int(np.pi / min(dx_rad, dy_rad)) + 100
    ell_max = min(ell_max, 15000)
    
    # Get power spectrum
    pars = camb.set_params(
        H0=H0_KM_S_MPC,
        ombh2=OMEGA_B0 * (H0_KM_S_MPC/100)**2,
        omch2=(OMEGA_M0 - OMEGA_B0) * (H0_KM_S_MPC/100)**2,
        tau=0.054,
        As=2.1e-9,
        ns=0.965,
        lmax=ell_max,
        lens_potential_accuracy=1 if lensed else 0
    )
    
    results = camb.get_results(pars)
    powers = results.get_cmb_power_spectra(pars, CMB_unit='muK')
    
    if lensed:
        Dl_raw = powers['total'][:, 0]
    else:
        Dl_raw = powers['unlensed_scalar'][:, 0]
    
    # Convert D_ℓ to C_ℓ
    ells = np.arange(len(Dl_raw))
    Cl_TT = np.zeros_like(Dl_raw)
    Cl_TT[2:] = Dl_raw[2:] * 2 * np.pi / (ells[2:] * (ells[2:] + 1))
    
    # Create 2D k-space grid
    kx = 2 * np.pi * np.fft.fftfreq(ny, d=dy_rad)
    ky = 2 * np.pi * np.fft.fftfreq(nx, d=dx_rad)
    KX, KY = np.meshgrid(ky, kx)
    ell_2d = np.sqrt(KX**2 + KY**2)
    
    # Interpolate C_ℓ to 2D grid
    Cl_2d = np.interp(ell_2d, ells, Cl_TT, left=0, right=0)
    
    # Generate Gaussian random field in Fourier space
    # Area in steradians
    area_sr = theta_x * theta_y
    
    # Power spectrum amplitude for FFT normalization
    # This matches the normalization in analysis_with_cmb.py
    amplitude = np.sqrt(Cl_2d * (2 * np.pi)**2 / area_sr / 2)
    
    # Generate complex Gaussian random field
    real_part = np.random.randn(ny, nx)
    imag_part = np.random.randn(ny, nx)
    cmb_fourier = amplitude * (real_part + 1j * imag_part)
    
    # Inverse FFT
    cmb_map = np.real(np.fft.ifft2(cmb_fourier)) * (ny * nx)
    
    # Enforce zero mean
    cmb_map -= cmb_map.mean()
    
    return cmb_map, chi_Mpc, theta_x, theta_y


def measure_map_power_spectrum(cmb_map, theta_x, theta_y, n_bins=100):
    """
    Measure the power spectrum of a flat-sky CMB map.
    
    Returns D_ℓ = ℓ(ℓ+1)C_ℓ/(2π) in µK².
    """
    ny, nx = cmb_map.shape
    
    # Pixel sizes in radians
    dx_rad = theta_x / nx
    dy_rad = theta_y / ny
    
    # Area in steradians
    area_sr = theta_x * theta_y
    
    # FFT (matching the inverse normalization used in generation)
    cmb_fft = np.fft.fft2(cmb_map) / (nx * ny)
    
    # Power spectrum: |FFT|² * area / (2π)²
    # This inverts the normalization: amplitude = sqrt(C_ℓ * (2π)² / area / 2)
    # So |amplitude|² = C_ℓ * (2π)² / area / 2
    # And |FFT|² = |amplitude|² * 2 (from real+imag parts)
    # Therefore C_ℓ = |FFT|² * area / (2π)²
    power_2d = np.abs(cmb_fft)**2 * area_sr / (2 * np.pi)**2 * 2
    
    # Create ℓ grid
    kx = 2 * np.pi * np.fft.fftfreq(ny, d=dy_rad)
    ky = 2 * np.pi * np.fft.fftfreq(nx, d=dx_rad)
    KX, KY = np.meshgrid(ky, kx)
    ell_2d = np.sqrt(KX**2 + KY**2)
    
    # Bin the power spectrum
    ell_max = ell_2d.max()
    ell_bins = np.linspace(0, ell_max, n_bins + 1)
    ell_centers = 0.5 * (ell_bins[:-1] + ell_bins[1:])
    
    Cl_binned = np.zeros(n_bins)
    counts = np.zeros(n_bins)
    
    for i in range(n_bins):
        mask = (ell_2d >= ell_bins[i]) & (ell_2d < ell_bins[i+1])
        if np.any(mask):
            Cl_binned[i] = np.mean(power_2d[mask])
            counts[i] = np.sum(mask)
    
    # Convert C_ℓ to D_ℓ
    Dl_binned = ell_centers * (ell_centers + 1) * Cl_binned / (2 * np.pi)
    
    # Only return bins with data
    valid = counts > 0
    
    return ell_centers[valid], Dl_binned[valid], Cl_binned[valid]


